    public void destroy() {
        SessionFactory sf = HibernateUtil.getSessionFactory();
        Session s = sf.openSession();
        Transaction tx = s.beginTransaction();
        
        try {
            s.delete(this.actor);
            tx.commit();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
            tx.rollback();
        }

        s.close();
    }
    
    public void save() {
        SessionFactory sf = HibernateUtil.getSessionFactory();
        Session s = sf.openSession();
        Transaction tx = s.beginTransaction();
        
        try {
            if (this.newRecord) {
                s.save(this.actor);
            } else {
                s.merge(this.actor);
            }
            
            tx.commit();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
            tx.rollback();
        }         
        
        s.clear();
    }
    
