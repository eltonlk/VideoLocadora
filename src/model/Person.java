package model;

public class Person {
    
    private int id;
    private String name;
    private String legal_name;
    private String kind;
    private String document_1;
    private String document_2;
    private String email;
    private String phone;
    private String cel;
    private String status;
    
    public Person() {
        
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLegal_name() {
        return legal_name;
    }

    public void setLegal_name(String legal_name) {
        this.legal_name = legal_name;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getDocument_1() {
        return document_1;
    }

    public void setDocument_1(String document_1) {
        this.document_1 = document_1;
    }

    public String getDocument_2() {
        return document_2;
    }

    public void setDocument_2(String document_2) {
        this.document_2 = document_2;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCel() {
        return cel;
    }

    public void setCel(String cel) {
        this.cel = cel;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
}
